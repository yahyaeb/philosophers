#include "philo.h"

int	ft_atoi(const char *str)
{
	size_t		i;
	int			sign;
	int			results;

	i = 0;
	sign = 1;
	results = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
	{
		sign = sign * -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		results = results * 10 + (str[i] - '0');
		i++;
	}
	return (results * sign);
}

int	is_max_or_min(int result, int sign)
{
	if ((sign == 1 && result > INT_MAX)
		|| (sign == -1 && (-result) < INT_MIN))
		return (1);
	else
		return (0);
}

int	is_within_range(char *str)
{
	int			i;
	int			sign;
	long long	result;

	i = 0;
	sign = 1;
	result = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
		return (0);
	else if (str[i] == '+')
		i++;
	if (!ft_isdigit(str[i]))
		return (0);
	while (str[i])
	{
		if (!ft_isdigit(str[i]))
			return (0);
		result = result * 10 + (str[i] - '0');
		if (is_max_or_min(result, sign))
			return (0);
		i++;
	}
	return (1);
}
